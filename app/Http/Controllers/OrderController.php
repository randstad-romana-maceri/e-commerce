    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $newOrder = new Order();
        $newOrder->user_id = 1;
        $newOrder->amount = $request["cart"][0]["price"] * $request["cart"][0]["quantity"];
        $newOrder->save();
        $newOrder->orderItems()->sync([["available_product_id" => 1, "quantity" => $request["cart"][0]["quantity"]]]);
        $availableProduct = AvailableProduct::find(1);
        $availableProduct->update(["quantity" => $availableProduct->quantity - $request["cart"][0]["quantity"]]);
    }

